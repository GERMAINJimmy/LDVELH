    <h1 class="my-3 text-center">Règles de combat</h1>
    <p>Au cours de votre quête, vous aurez maintes fois l'occasion
        d'affronter un ennemi. Le texte vous indiquera en chaque
        circonstance quels sont les points d'HABiLETÉ et
        d'ENDURANCE de l'ennemi en question. Vous devrez vous
        efforcer de tuer votre adversaire en réduisant à zéro ses points
        d'ENDURANCE, tout en essayant d'en perdre vous-même le
        moins possible au cours de l'affrontement.
        Au début de chaque combat, inscrivez votre total
        d'ENDURANCE et celui de l'ennemi sur votre Feuille
        d'Aventure. Ces indications sont à reporter dans la case Détails
        des Combats. Chaque affrontement se déroule de la manière
        suivante :</p>

    <p>1. Ajoutez à votre total d'HABiLETÉ les points supplémentaires
        que certaines Grandes Disciplines et certains Objets Spéciaux
        peuvent vous valoir.</p>

    <p>2. Soustrayez du total ainsi obtenu les points d'HABiLETÉ de
        votre adversaire.</p>

    <p>Le résultat de cette soustraction vous donnera votre Quotient
        d'Attaque. Inscrivez-le sur votre Feuille d'Aventure.</p>

    <p class="fw-bold">Exemple</p>

    <p>Imaginons que vous disposiez d'une HABILETÉ de 32. Vous êtes
        attaqué par une meute de Chiens Sauvages dont le total
        d'HABiLETÉ se monte à 31 et, pris par surprise, vous n'avez
        aucune possibilité de fuite. Si vous disposez de la Grande
        Discipline du Foudroiement Psychique contre laquelle les Chiens
        Sauvages ne sont pas immunisés, vous ajoutez 8 points à votre
        total d'HABiLETÉ. Si vous possédez en outre l'arme Kaï
        Ulnarias, vous ajouterez 5 points supplémentaires à votre total
        d'HABiLETÉ qui atteindra finalement 45. Retranchez ensuite le
        total d'HABiLETÉ des Chiens Sauvages de votre propre total, ce
        qui vous donnera un Quotient d'Attaque de 14 (45 - 31 = 14).
        Inscrivez 14 dans la case Quotient d'Attaque de la Feuille
        d'Aventure.</p>

    <p>3. Lorsque vous connaissez votre Quotient d'Attaque, utilisez la
        <a href="table_de_hasard.jpg">Table de hasard</a> de la façon habituelle pour obtenir un chiffre.</p>

    <p>4. Reportez-vous à la Table des Coups Portés qui figure à la fin
        de ce livre. En haut de cette table sont indiqués les Quotients
        d'Attaque. Trouvez celui que vous avez calculé, puis descendez la
        colonne jusqu'à la case située à l'intersection de la ligne
        horizontale correspondant au chiffre obtenu sur la <a href="table_de_hasard.jpg">Table de hasard</a> (ces
        chiffres sont indiqués sur le côté gauche de la Table
        des Coups Portés). Vous connaissez ainsi le nombre de points
        d'ENDURANCE que l'ennemi et vous-même aurez perdu lors
        de cet Assaut. (La lettre E vous donne les points
        d'ENDURANCE perdus par l'ennemi, les lettres LS, ceux
        perdus par vous, disciple de Loup Solitaire.)</p>

    <p class="fw-bold">Exemple</p>

    <p>Le Quotient d'Attaque entre la meute de Chiens Sauvages et vous
        est de 14. Si le chiffre donné par la <a href="table_de_hasard.jpg">Table de hasard</a> est de 1, le
        résultat du premier Assaut sera alors le suivant : vous (LS)
        perdez 3 points d'ENDURANCE (plus 1 point que vous coûte
        l'usage du Foudroiement Psychique). La meute des Chiens
        Sauvages perd 9 points d'ENDURANCE.</p>

    <p>5. Après chaque Assaut, notez sur la Feuille d'Aventure les
        modifications intervenues dans le total d'ENDURANCE
        de chaque adversaire.</p>

    <p>6. A moins que de nouvelles indications vous soient données ou
        que vous ayez la possibilité de fuir, un nouvel Assaut devra être
        livré.</p>

    <p>7. Reprenez les opérations à partir de l'étape n° 3. Le combat doit
        se poursuivre jusqu'à ce que le total d'ENDURANCE de
        l'ennemi ou le vôtre ait été réduit à zéro, ce qui signifie la mort.
        Si vous mourez, l'aventure s'achève. Si votre ennemi périt, vous poursuivez votre quête avec un total
        d'ENDURANCE réduit du nombre de points que vous avez perdus au cours du combat.</p>
    <h1 class="my-3 text-center">Possibilités de fuite</h1>
    <p>Au cours de votre aventure, la possibilité d'échapper à un combat
        vous sera parfois accordée. Si vous avez déjà engagé un Assaut et
        décidez de prendre la fuite, menez-le d'abord à son terme en
        observant les règles habituelles. </p>

    <p>Il ne sera tenu aucun compte des points d'ENDURANCE perdus par votre adversaire au cours de
        cet Assaut. Seuls les vôtres devront être comptabilisés et
        retranchés de votre total d'ENDURANCE, car tel est le prix de
        la couardise ! </p>

    <p>Rappelez-vous que vous ne pourrez vous enfuir que
        si vous y êtes autorisé ; les précisions nécessaires vous seront
        fournies chaque fois.</p>

