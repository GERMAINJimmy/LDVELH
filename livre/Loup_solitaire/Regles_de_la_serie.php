    <h1 class="my-3 text-center">Règles de la série</h1>
    <p>Vous trouverez au début de ce livre une <a href="#">Feuille de personnage</a> sur laquelle vous
        inscrirez tous les détails de votre quête. Il est
        conseillé d'en faire des photocopies qui vous permettront de jouer plusieurs fois.</p>

    <p>Au cours des années passées au monastère, vous avez développé
        vos aptitudes au combat (HABILETÉ) et votre forme physique
        (ENDURANCE).</p>

    <p>Avant de vous lancer dans cette aventure du
        Second Ordre Kaï, il vous faut évaluer avec exactitude l'efficacité
        de cet entraînement.</p>

    <p>Placez devant vous la <a href="table_de_hasard.jpg">Table de hasard</a> qui
        se trouve à la fin du livre, fermez les yeux et pointez un crayon. Si
        vous tombez sur le chiffre 0, vous n'obtenez aucun point.</p>

    <p>Le premier chiffre que votre crayon aura montré sur la Table de
        Hasard représentera votre HABILETÉ au combat. Ajoutez 25 à ce
        chiffre et inscrivez le total obtenu dans la case HABILETÉ de votre
        Feuille d'Aventure (si par exemple votre crayon indique le chiffre
        7 sur la Table de Hasard, vos points d'HABILETÉ seront de 32).</p>

    <p>Lorsque vous aurez à combattre, il faudra mesurer votre
        HABILETÉ à celle de votre adversaire. D est donc souhaitable que
        votre total d'HABILETÉ soit le plus élevé possible. </p>

    <p>Le second chiffre que vous désignerez sur la Table de Hasard représentera
        votre capacité d'ENDURANCE. Ajoutez 30 à ce chiffre et inscrivez le
        total obtenu dans la case ENDURANCE de votre Feuille d'Aventure
        (à titre d'exemple, si votre crayon indique le chiffre 8 sur la Table
        de Hasard, le total de vos points d'ENDURANCE sera de 38). </p>

    <p>Si vous êtes blessé lors d'un combat, vous perdez des points
        d'ENDURANCE, et si jamais votre ENDURANCE tombe à 0, vous
        saurez alors que votre aventure est terminée. </p>

    <p>Au cours de votre mission, vous aurez la possibilité de récupérer des points
        d'ENDURANCE, mais votre total d'ENDURANCE ne pourra en aucun
        cas dépasser celui dont vous disposiez au départ de votre
        mission.</p>

    <p>Si vous avez mené avec succès l'une des précédentes aventures, vous pouvez reporter vos
        totaux d'HABILETÉ et d'ENDURANCE dans ce volume. Vous pouvez
        aussi emporter tous les Objets Spéciaux, Armes Kaï et objets
        contenus dans votre sac à dos qui se trouvaient en votre
        possession à la fin de votre précédente aventure, et que vous
        devez donc inscrire en détail sur votre Feuille d'Aventure. Une
        astuce : si vous trouvez cela plus pratique, vous pouvez utiliser
        un dé à dix faces à la place de la Table de Hasard.</p>

